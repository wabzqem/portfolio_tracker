const fs = require('fs');

// Create a simple 1x1 PNG file as a placeholder (transparent pixel)
// This is a minimal valid PNG file
const pngData = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, // IHDR chunk length
  0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x00, 0x10, // Width: 16
  0x00, 0x00, 0x00, 0x10, // Height: 16
  0x08, 0x06, 0x00, 0x00, 0x00, // Bit depth, color type, compression, filter, interlace
  0x1F, 0xF3, 0xFF, 0x61, // CRC
  0x00, 0x00, 0x00, 0x0B, // IDAT chunk length
  0x49, 0x44, 0x41, 0x54, // IDAT
  0x08, 0x1D, 0x01, 0x00, 0x00, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01,
  0x00, 0x00, 0x00, 0x00, // IEND chunk length
  0x49, 0x45, 0x4E, 0x44, // IEND
  0xAE, 0x42, 0x60, 0x82  // CRC
]);

// Write the PNG file
fs.writeFileSync('assets/icon.png', pngData);
console.log('Created simple PNG icon file');